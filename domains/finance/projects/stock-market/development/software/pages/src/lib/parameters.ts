export const DOMAIN_ORDER = ['macro', 'momentum', 'volatility', 'fundamental', 'cross_asset'];

export const DOMAIN_LABELS: Record<string, string> = {
  macro:       'Macro & Monetary',
  momentum:    'Momentum & Technical',
  volatility:  'Volatility',
  fundamental: 'Fundamental',
  cross_asset: 'Cross-Asset',
};

export function groupByDomain(params: Parameter[]): Map<string, Parameter[]> {
  const map = new Map<string, Parameter[]>();
  for (const d of DOMAIN_ORDER) map.set(d, []);
  for (const p of params) {
    if (!map.has(p.domain)) map.set(p.domain, []);
    map.get(p.domain)!.push(p);
  }
  return map;
}
